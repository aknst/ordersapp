package mirea.prac16.GUI.components;

public class SelectItem {
    private String key;
    private int value;

    public SelectItem(String key, int value)
    {
        this.key = key;
        this.value = value;
    }

    @Override
    public String toString()
    {
        return key;
    }

    public String getKey()
    {
        return key;
    }

    public int getValue()
    {
        return value;
    }
}
